/**
 * Generate a chart image URL via quickchart.io from a Chart.js config object.
 * Used for IELTS Task 1 prompts so the image actually matches the data the
 * student is asked to describe (otherwise AI grading is meaningless).
 */
function chartUrl(config) {
  const params = new URLSearchParams({
    width: "900",
    height: "560",
    backgroundColor: "white",
    c: JSON.stringify(config),
  });
  return `https://quickchart.io/chart?${params}`;
}

