function buildBody(entry) {
  const body = {
    title: entry.title,
    level: entry.level,
    topic: entry.topic,
    description: entry.description,
    examType: entry.examType,
    examPrompt: entry.examPrompt,
  };
  if (entry.imageUrl) body.imageUrl = entry.imageUrl;
  return body;
}

async function runUpdateImages() {
  console.log("Mode: UPDATE_IMAGES (patch imageUrl on existing exams)\n");
  let updated = 0;
  let missing = 0;
  let unchanged = 0;
  let failed = 0;

  for (const entry of EXAMS) {
    if (!entry.imageUrl) continue;
    try {
      const result = await Exam.updateOne(
        { title: entry.title, imageUrl: { $ne: entry.imageUrl } },
        { $set: { imageUrl: entry.imageUrl } },
      );
      if (result.matchedCount === 0) {
        const exists = await Exam.exists({ title: entry.title });
        if (exists) {
          unchanged++;
          console.log(`SAME  ${entry.title}`);
        } else {
          missing++;
          console.log(`MISS  ${entry.title} (no exam with this title)`);
        }
      } else {
        updated++;
        console.log(`UPD   ${entry.title}`);
      }
    } catch (err) {
      failed++;
      console.error(`FAIL  ${entry.title}: ${err.message}`);
    }
  }

  console.log(
    `\nSummary: updated=${updated}, unchanged=${unchanged}, missing=${missing}, failed=${failed}`,
  );
}

async function run() {
  await CONNECT_DB();

  if (UPDATE_IMAGES) {
    await runUpdateImages();
    await mongoose.disconnect();
    return;
  }

  console.log(
    `Mode: ${FORCE ? "FORCE (allow duplicate titles)" : "DEDUPE by title"}`,
  );
  console.log(`Planning ${EXAMS.length} exams.\n`);

  let created = 0;
  let skipped = 0;
  let failed = 0;

  for (const entry of EXAMS) {
    try {
      if (!FORCE) {
        const exists = await Exam.exists({ title: entry.title });
        if (exists) {
          skipped++;
          console.log(`SKIP  [${entry.level}/${entry.examType}] ${entry.title}`);
          continue;
        }
      }
      const exam = await createExam(buildBody(entry));
      created++;
      console.log(
        `OK    [${entry.level}/${entry.examType}] ${entry.title} → ${exam._id}`,
      );
    } catch (err) {
      failed++;
      console.error(`FAIL  ${entry.title}: ${err.message}`);
    }
  }

  console.log(
    `\nSummary: created=${created}, skipped=${skipped}, failed=${failed}`,
  );
  await mongoose.disconnect();
}

run().catch((e) => {
  console.error(e);
  process.exit(1);
});
